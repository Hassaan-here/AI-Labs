# Write a Python program that demonstrates different data types and type casting.

# Integer
x = 5
print(type(x))
print(x)

# Float
y = 5.25
print(type(y))
print(y)

# String
z = "Hello"
print(type(z))
print(z)

# Boolean
a = True
print(type(a))
print(a)

# Type casting
print("type casting")
b = int(y)  # Converting float to integer
print(type(b))
print(b)
